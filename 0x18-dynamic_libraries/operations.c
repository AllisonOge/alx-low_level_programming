/* operations.c */

/**
 * add - add two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - implements the sub operation
 * @a: first integer
 * @b: second integer
 *
 * Return: difference of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - implements the mul operation
 * @a: first integer
 * @b: second integer
 *
 * Return: product of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - implements the div operation
 * @a: first integer
 * @b: second integer
 *
 * Return: division of a by b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - implements the mod operation
 * @a: first integer
 * @b: second integer
 *
 * Return: remainder of the division of a by b
 */
int mod(int a, int b)
{
	return (a % b);
}
